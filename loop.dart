void main() {
  //for loop
  for (int i = 1; i <= 10; i++) {
    print("$i");
  }
  //while loop
  int i = 1; //data type
  while (i <= 10) {
    //loop
    print("$i"); // kaj
    i++; // incriment /dicrement
  }
  // do while loop
  int k = 1;
  do {
    print("$k"); //kaj
    k++; //incriment /dicriment
  } while (k <= 10); //loop
}
